; setting the colors
FRG "#3A6EA5"

MOVV R1 100
MOVV R2 100
; R1 = x
; R2 = y

.GAMELOOP:
CLSC "#004E98"
GRKEY R0

MOVV R3 1
JE R3 R0 .MOVE_UP

MOVV R3 2
JE R3 R0 .MOVE_RIGHT

MOVV R3 3
JE R3 R0 .MOVE_DOWN

MOVV R3 4
JE R3 R0 .MOVE_LEFT

JP .STOP

.STOP:
MOVV R4 0
JP .RENDER

.MOVE_UP:
MOVV R4 4
SUB R2 R4
JP .RENDER

.MOVE_RIGHT:
MOVV R4 4
ADD R1 R4
JP .RENDER

.MOVE_DOWN:
MOVV R4 4
ADD R2 R4
JP .RENDER

.MOVE_LEFT:
MOVV R4 4
SUB R1 R4
JP .RENDER

.RENDER:
MOVV R4 0
PLOTR R1 R2 10
JP .GAMELOOP

; unreacheable
HALT
